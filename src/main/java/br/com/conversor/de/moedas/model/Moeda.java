        private static final Map<String, Moeda> lookup = new HashMap<>();

        static {
                for (Moeda m : Moeda.values()) {
                        lookup.put(m.codigo.toUpperCase(), m);
                }
        }

        public static boolean existeCodigo(String cod) {
                return lookup.containsKey(cod.toUpperCase());
        }

        public static Moeda fromCodigo(String cod) {
                return lookup.get(cod.toUpperCase());
        }

